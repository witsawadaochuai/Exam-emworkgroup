-- โจทย์: หาพนักงานกะ Night ของ "วันที่ทำงาน 2026-03-19" ที่มาสาย
--        (Clock-in หลัง 00:05 น.) โดยต้องรองรับกรณีสแกนนิ้วก่อนเที่ยงคืน
--        (เช่น 23:55 ของวันที่ 18 มี.ค.) เพื่อเข้ากะวันที่ 19
--
-- แนวคิดหลัก: กะ Night ของ "วันทำงาน D" จริง ๆ คือช่วง D 00:00 -> D 08:00
--             แต่พนักงานมักสแกนเข้าก่อนเที่ยงคืนของ D-1 (เข้าก่อนเริ่มกะได้)
--             => กำหนด "ช่วงเวลาสแกนที่ถือว่าเป็นกะของวันที่ 19"
--                = ตั้งแต่เย็นวันที่ 18 (เช่น 23:00) ไปจนจบกะ 19 เวลา 08:00
--             => "สาย" ตัดสินจาก "การสแกนครั้งแรก (MIN) เกิน 00:05 ของวันที่ 19"
--                คนที่สแกน 23:55 ของวันที่ 18 -> MIN < 00:05 -> ไม่สาย (ถูกต้อง)
--
-- สมมติฐานตาราง:
--   employees(emp_id, full_name)
--   shift_assignments(emp_id, work_date DATE, shift_type)  -- 'MORNING'|'EVENING'|'NIGHT'
--   attendance(emp_id, clock_in TIMESTAMP)                 -- เวลาสแกนจริง
-- ============================================================================

SELECT a.emp_id,
       e.full_name,
       MIN(a.clock_in) AS actual_clock_in
FROM   shift_assignments sa
JOIN   employees   e ON e.emp_id = sa.emp_id
JOIN   attendance  a ON a.emp_id = sa.emp_id
WHERE  sa.shift_type = 'NIGHT'
  AND  sa.work_date  = DATE '2026-03-19'
       -- ช่วงสแกนที่นับเป็นกะ Night ของวันที่ 19 (รองรับสแกนก่อนเที่ยงคืน):
  AND  a.clock_in >= TIMESTAMP '2026-03-18 23:00:00'   -- เย็นวันที่ 18 (เข้าก่อนกะ)
  AND  a.clock_in <  TIMESTAMP '2026-03-19 08:00:00'   -- จบกะ 08:00 วันที่ 19
GROUP BY a.emp_id, e.full_name
       -- มาสาย: การสแกน "ครั้งแรก" เกิน 00:05 ของวันที่ 19
HAVING MIN(a.clock_in) > TIMESTAMP '2026-03-19 00:05:00';

-- ----------------------------------------------------------------------------
-- ทำไมใช้ MIN(clock_in) + HAVING:
--   * พนักงานอาจสแกนหลายครั้ง การสแกน "ครั้งแรกสุด" คือเวลาเข้างานจริง
--   * คนที่สแกน 23:55 (18 มี.ค.) -> MIN = 23:55 < 00:05 -> ไม่เข้าเงื่อนไข HAVING -> ไม่สาย
--   * คนที่สแกนครั้งแรก 00:10 (19 มี.ค.) -> MIN = 00:10 > 00:05 -> สาย (ติดผลลัพธ์)
